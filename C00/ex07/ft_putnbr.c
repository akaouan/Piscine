#include<unistd.h>

void	ft_putnbr(int nb)
{
	char	number;
	long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	number = 48 + (n % 10);
	write(1, &number, 1);
}
