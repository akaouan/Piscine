#include<unistd.h>
void	ft_putnbr(int nb)
{
	long	n;
	char	number;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	n %= 10;
	number = n + '0';
	write(1, &number, 1);
}
